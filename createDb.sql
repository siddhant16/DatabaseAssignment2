---CREATE TABLE QUERIES---

CREATE TABLE YELP_USER(
    Yelp_user_id varchar(3),
    Email varchar(25) NOT NULL,
    FirstName varchar(15) NOT NULL,
    LastName varchar(15) NOT NULL,
    DOB DATE NOT NULL,
    BirthPlace varchar(20),
    Gender char(1) NOT NULL,
    CONSTRAINT YELP_USER_PK PRIMARY KEY(Yelp_user_id)
);

CREATE TABLE YELP_USER_FRIENDLIST(
    Yelp_user_id varchar(3),
    Yelp_friend_id varchar(3) NOT NULL,
    CONSTRAINT USR_FRNDLST PRIMARY KEY (Yelp_user_id, Yelp_friend_id),
    CONSTRAINT USR_FRNDLST_FK_Yelp_user_id FOREIGN KEY (Yelp_user_id) REFERENCES YELP_USER(Yelp_user_id) ON DELETE CASCADE,
    CONSTRAINT USR_FRNDLST_FK_Yelp_frnd_id FOREIGN KEY (Yelp_friend_id) REFERENCES YELP_USER(Yelp_user_id) ON DELETE CASCADE
);

CREATE TABLE YELP_COMPLIMENTED_FRIENDLIST(
    Yelp_user_id varchar(3),
    Yelp_friend_id varchar(3) NOT NULL,
    CONSTRAINT Com_Frndlst_PK PRIMARY KEY (Yelp_user_id, Yelp_friend_id),
    CONSTRAINT Com_Frndlst_FK_Yelp_user_id FOREIGN KEY (Yelp_user_id) REFERENCES YELP_USER(Yelp_user_id) ON DELETE CASCADE ,
    CONSTRAINT Com_Frndlst_FK_Yelp_friend_id FOREIGN KEY (Yelp_friend_id) REFERENCES YELP_USER(Yelp_user_id) ON DELETE CASCADE
);

CREATE TABLE YELP_BUSINESS_CATEGORY(
    Business_cat_id varchar(5),
    Business_cat_name varchar(25),
    CONSTRAINT YELP_BUSINESS_CATEGORY_PK PRIMARY KEY (Business_cat_id) 
);

CREATE TABLE YELP_BUSINESS_SUB_CATEGORY (
    Business_cat_id varchar(5),
    Business_sub_cat_name varchar(100),
    constraint BNESS_SUB_CAT_PK PRIMARY KEY (Business_cat_id, Business_sub_cat_name),
    constraint BNESS_SUB_CAT_FK_Bus_cat_id FOREIGN KEY (Business_cat_id) REFERENCES YELP_BUSINESS_CATEGORY(Business_cat_id) ON DELETE CASCADE 
);

CREATE TABLE YELP_BUSINESS(
    Business_id varchar(5),
    Business_name varchar(35),
    Days_Of_Operation varchar(50),
    Ambient varchar(20),
    Business_cat_id varchar(7),
    CONSTRAINT Business_PK PRIMARY KEY(Business_id),
    CONSTRAINT Business_FK_Business_cat_id FOREIGN KEY (Business_cat_id) REFERENCES YELP_BUSINESS_CATEGORY(Business_cat_id) ON DELETE CASCADE
);

CREATE TABLE YELP_BUSINESS_ADDRESS(
    Business_id varchar(5),
    Street_no varchar(15),
    Street_name varchar(50),
    City varchar(15),
    State varchar(15),
    ZipCode number,
    CONSTRAINT YELP_BUSINESS_ADDRESS_PK PRIMARY KEY(Business_id),
    CONSTRAINT YELP_BUSINESS_ADDRESS_FK FOREIGN KEY (Business_id) REFERENCES YELP_BUSINESS(Business_id) ON DELETE CASCADE
); 


CREATE TABLE YELP_CHECKIN(
    Checkin_id varchar(5),
    Yelp_user_id varchar(3),
    Business_id varchar(5),
    Info varchar(100),
    CONSTRAINT YELP_CHECKIN_PK PRIMARY KEY (checkin_id),
    CONSTRAINT YELP_CHECKIN_FK_Yelp_user_id FOREIGN KEY (Yelp_user_id) REFERENCES YELP_USER(Yelp_user_id) ON DELETE CASCADE,
    CONSTRAINT YELP_CHECKIN_FK_Business_id FOREIGN KEY (Business_id) REFERENCES YELP_BUSINESS(Business_id) ON DELETE CASCADE
);

CREATE TABLE YELP_REVIEWS(
    Review_id varchar(5),
    No_of_Stars number,
    Author varchar(5),
    Publish_Date date,
    Business_id varchar(5),
    CONSTRAINT Review_PK PRIMARY KEY (REVIEW_ID),
    CONSTRAINT Review_FK_Author FOREIGN KEY (Author) REFERENCES YELP_USER(Yelp_user_id) ON DELETE CASCADE,
    CONSTRAINT Review_FK_Business_id FOREIGN KEY (Business_id) REFERENCES YELP_BUSINESS(Business_id) ON DELETE CASCADE
);


CREATE TABLE YELP_LIST_OF_COMMENTS(
    Review_id varchar2(3),
    Yelp_user_id varchar(3),
    Comments varchar(150),
    CONSTRAINT Lst_Comments_PK PRIMARY KEY (Review_id, Yelp_user_id),
    CONSTRAINT Lst_Comments_FK_Yelp_user_id FOREIGN KEY (Yelp_user_id) REFERENCES YELP_USER(Yelp_user_id) ON DELETE CASCADE,
    CONSTRAINT Lst_Comments_FK_Review_id FOREIGN KEY (Review_id) REFERENCES YELP_REVIEWS(Review_id) ON DELETE CASCADE
);


